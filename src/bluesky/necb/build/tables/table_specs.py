"""NECB Table Specifications

This module loads table specifications from JSON files generated by the scanner.
The JSON files contain comprehensive table metadata for all NECB vintages.

The specifications define which tables exist in each vintage and their page
locations (0-indexed). The database builder uses these to parse and extract
table data.

Note: Page numbers are 0-indexed (PDF page 1 = index 0)
"""

import json
from functools import lru_cache
from pathlib import Path
from typing import Any


# Path to JSON specification files
# File is at: .../bluesky/src/bluesky/mcp/scrapers/necb/parsers/tables/table_specs.py
# Data is in parsers/tables/data/
SPECS_DIR = Path(__file__).parent / "data"


@lru_cache(maxsize=4)
def load_table_specs_from_json(vintage: str) -> dict[str, Any]:
    """
    Load table specifications from JSON file for a given vintage

    Args:
        vintage: NECB vintage ("2011", "2015", "2017", "2020")

    Returns:
        Dictionary containing table specifications with metadata

    Raises:
        FileNotFoundError: If JSON file for vintage not found
        json.JSONDecodeError: If JSON file is malformed
    """
    json_path = SPECS_DIR / f"table-specs-{vintage}.json"

    if not json_path.exists():
        raise FileNotFoundError(
            f"Table specifications for vintage {vintage} not found at {json_path}. "
            f"Run: python scripts/scan_all_necb_tables.py --vintage {vintage}"
        )

    with open(json_path, "r") as f:
        data = json.load(f)

    # Scanner output format: {"vintage": {"total_found": N, "tables": {...}}}
    if vintage in data:
        return data[vintage]
    else:
        # Fallback if JSON is just the table data
        return data


def get_table_specs(vintage: str) -> list[dict[str, Any]]:
    """
    Get table specifications for a given vintage

    Returns list of table dictionaries with full scanner metadata including:
    - table_number: Table identifier (e.g., "3.2.2.2")
    - page_num: 0-indexed page number
    - page_display: 1-indexed page number (for display)
    - page_start, page_end: Multi-page table support
    - all_pages: List of all pages for multi-page tables
    - page_count: Number of pages
    - title_text: Table title
    - table_description: Description text
    - references: Forming part references
    - has_forming_part, has_continuation: Boolean flags
    - estimated_rows, estimated_cols: Table dimensions
    - confidence, score: Detection confidence metrics

    Args:
        vintage: NECB vintage ("2011", "2015", "2017", "2020")

    Returns:
        List of table specification dictionaries with full metadata

    Raises:
        FileNotFoundError: If JSON file for vintage not found
        KeyError: If vintage not found in data
    """
    data = load_table_specs_from_json(vintage)

    # Extract tables from scanner format
    if "tables" in data:
        # Convert from dict of tables to list
        return list(data["tables"].values())
    else:
        # Fallback if data is already a list
        return data


def get_all_vintages() -> list[str]:
    """
    Get list of all available vintages

    Scans the docs/necb directory for table-specs-*.json files

    Returns:
        List of vintage strings (e.g., ["2011", "2015", "2017", "2020"])
    """
    if not SPECS_DIR.exists():
        return []

    vintages = []
    for json_file in SPECS_DIR.glob("table-specs-*.json"):
        # Extract vintage from filename: table-specs-2020.json -> 2020
        vintage = json_file.stem.replace("table-specs-", "")
        vintages.append(vintage)

    return sorted(vintages)


def get_table_count(vintage: str) -> int:
    """
    Get total number of tables available for a vintage

    Args:
        vintage: NECB vintage ("2011", "2015", "2017", "2020")

    Returns:
        Number of tables found

    Raises:
        FileNotFoundError: If JSON file for vintage not found
    """
    data = load_table_specs_from_json(vintage)

    if "total_found" in data:
        return data["total_found"]
    elif "tables" in data:
        return len(data["tables"])
    else:
        return len(data)
